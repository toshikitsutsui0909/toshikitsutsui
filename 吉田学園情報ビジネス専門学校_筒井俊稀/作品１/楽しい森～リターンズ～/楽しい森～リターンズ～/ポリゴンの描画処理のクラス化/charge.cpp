#include "charge.h"
#include "gauge.h"

int CCharge::m_nCount = 0;
CGauge *CCharge::m_pGauge = NULL;
//=============================================================================
//	コンストラクタ
//=============================================================================
CCharge::CCharge(int nPriority) : CScene(nPriority)
{
}

//=============================================================================
//	デストラクタ
//=============================================================================
CCharge::~CCharge()
{
}

//=============================================================================
//	生成処理
//=============================================================================
CCharge * CCharge::Create(D3DXVECTOR3 pos, D3DXVECTOR3 size)
{
	CCharge *pCharg;
	pCharg = new CCharge;
	if (pCharg != NULL)
	{
		pCharg->Init(pos, size);
	}
	return nullptr;
}

//=============================================================================
//	初期化処理
//=============================================================================
HRESULT CCharge::Init(D3DXVECTOR3 pos, D3DXVECTOR3 size)
{
	SetObjType(OBJTYPE_CHARGE);
	m_pGauge = CGauge::Create(D3DXVECTOR3(500.0f, 630.0f, 0.0f), D3DXVECTOR3(5.1f,10.0f,0.0f));
	return S_OK;
}

//=============================================================================
//	終了処理
//=============================================================================
void CCharge::Uninit(void)
{
	if (m_pGauge != NULL)
	{
		m_pGauge->Uninit();
		delete m_pGauge;
		m_pGauge = NULL;
	}

	Release();
}

//=============================================================================
//	更新処理
//=============================================================================
void CCharge::Update(void)
{
}

//=============================================================================
//	描画処理
//=============================================================================
void CCharge::Draw(void)
{
	if (m_pGauge != NULL)
	{
		m_pGauge->Draw();
	}
}

//=============================================================================
//	加算処理
//=============================================================================
void CCharge::Add(void)
{
	m_nCount++;
	if (m_nCount >= 60)
	{
		m_nCount = 60;
	}
	if (m_pGauge != NULL)
	{
		m_pGauge->SetNum(m_nCount);
	}

}

//=============================================================================
//	チャージ設定
//=============================================================================
void CCharge::Set(void)
{
	if (m_pGauge != NULL)
	{
		m_nCount = 0;
		m_pGauge->SetNum(m_nCount);
	}
}
